

fun main() {

    /**
     * Datatype is used to define type of variable
     * There are different datatype in Kotlin
     * - Int
     * - Boolean
     * - Double
     * - Float
     * - Long
     * - Char
     * - Short
     */

    val i: Int = 10
    val b: Boolean = true
    val d: Double = 10.005453254345346
    val f: Float = 20.2f
    val l: Long = 1000000L
    val c: Char = 'A'
    val digitChar: Char = '$'
    val short:Short = 10


    println(i)
    println(b)
    println(d)
    println(f)
    println(l)
    println(c)
    println("Digit char is $digitChar")
    println(short)
}